package ru.shark.home.common.services.dto;

public class PageRequest extends ListRequest {
    private int page;
    private int size;


    public PageRequest() {
        // empty constructor
    }

    public PageRequest(int page, int size) {
        this.page = page;
        this.size = size;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
